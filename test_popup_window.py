#!/usr/bin/env python3
"""
Тестовый скрипт для проверки работы PopupWindow с текстами разной длины
"""

import sys
import time
from PyQt6.QtWidgets import QApplication
from GUIClient.Windows.popup_window import PopupWindow
from Utility import ConfigManager

def test_popup_window():
    """Тестирование PopupWindow с текстами разной длины"""
    
    # Создаем приложение
    app = QApplication(sys.argv)
    
    # Создаем конфигурацию
    config = ConfigManager()
    
    # Создаем popup окно
    popup = PopupWindow()
    
    # Тестовые тексты разной длины
    test_texts = [
        "Короткий текст",  # Короткий
        "Это средний текст для проверки отображения в окне",  # Средний
        "Это длинный текст который должен вызывать расширение окна и появление скролла. Он содержит много слов и должен быть полностью виден пользователю.",  # Длинный
        "Короткий текст",  # Короткий
        "Очень длинный текст который точно не поместится в минимальный размер окна. Этот текст должен вызвать расширение окна до максимального размера и появление скроллбара для прокрутки всего содержимого. Пользователь должен иметь возможность прочитать весь текст без проблем.",  # Очень длинный
        "Короткий текст",  # Короткий
        "Экстремально длинный текст который содержит целую историю или документ. Этот текст настолько длинный что даже максимальный размер окна не сможет его полностью показать без скролла. Пользователь сможет прокручивать текст чтобы прочитать всё содержимое. Это должно работать корректно и плавно.",  # Экстремально длинный
        "Короткий текст",  # Короткий
    ]
    
    print("Тестирование PopupWindow с текстами разной длины...")
    print("Нажмите Enter для следующего текста, 'q' для выхода")
    
    for i, text in enumerate(test_texts):
        print(f"\nТест {i+1}: Текст длиной {len(text)} символов")
        print(f"Текст: {text[:50]}{'...' if len(text) > 50 else ''}")
        
        # Показываем окно
        popup.start_recording()
        popup.show()
        
        # Устанавливаем результат
        popup.set_result(text)
        
        # Ждем подтверждения
        user_input = input("Нажмите Enter для продолжения или 'q' для выхода: ")
        if user_input.lower() == 'q':
            break
        
        # Скрываем окно
        popup.fade_out()
        time.sleep(0.5)
    
    print("Тестирование завершено")
    popup.hide()
    app.quit()

if __name__ == "__main__":
    test_popup_window()
